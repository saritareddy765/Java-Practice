package com.Basics;

public class DataTypes {
    int a = 10;
    String s = "Java";
    char c = 'a';
    double d = 2.32;
    float num = 2.25f;
    boolean bool = true;
    byte b = 20;
    short sh = 250;
    long l = 50L;

    public static void main(String[] args) {
        DataTypes dataTypes = new DataTypes();
        System.out.println(dataTypes.a);
        System.out.println(dataTypes.sh);
        System.out.println(dataTypes.num);
        System.out.println(dataTypes.bool);
    }
}
